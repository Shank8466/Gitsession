# Break
a = "String"
for i in a:
    if i == "i":
        break   
    else:
        print(i)
else: 
    print("this has break")


# Continue
a = "String"
for i in a:
    if i == "i":
        continue   
    else:
        print(i)
else: 
    print("this has continue")


